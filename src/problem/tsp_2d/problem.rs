use rand::seq::SliceRandom;
use std::collections::{HashMap, HashSet};
use std::fs::File;
use std::io::{BufRead, BufReader};

use crate::error::OptError;
use crate::search_state::{ProblemTrait, Rankable};

/// A sequence of city indices representing a Hamiltonian tour (0-indexed).
pub type TspTour = Vec<usize>;
pub type TspEdge = (usize, usize);

/// A solution to the TSP problem.
///
/// - `tour` — ordered sequence of city indices forming the tour
/// - `objective` — total tour length
/// - `gain` — cached 2-opt gains keyed by directed edge pair `((a, b), (c, d))`;
///   value = change in tour length when removing edges `a→b` and `c→d` (negative = improvement).
#[derive(Debug, Clone)]
pub struct TspSolution {
    /// Ordered sequence of city indices representing the tour.
    pub tour: TspTour,
    /// Total tour length.
    pub objective: f64,
    /// Cached 2-opt gains keyed by directed edge pair `((a, b), (c, d))`.
    pub gain: HashMap<(TspEdge, TspEdge), f64>,
}

impl Rankable for TspSolution {
    fn is_better_than(&self, other: &Self) -> bool {
        self.objective < other.objective
    }
}

impl ProblemTrait for TspWithCoordinates {
    type Solution = TspSolution;

    fn new_solution(&self, rng: &mut impl rand::Rng) -> TspSolution {
        let mut tour: TspTour = (0..self.get_n()).collect();
        tour.shuffle(rng);
        let objective = self
            .calculate_tour_length(&tour)
            .expect("tour generated by new_solution should always be valid");
        let gain = self.compute_all_gains(&tour);
        TspSolution {
            tour,
            objective,
            gain,
        }
    }
}

/// TSP problem instance storing city coordinates.
///
/// Distances are computed as Euclidean distances between 2D coordinate pairs.
#[derive(Debug, Clone)]
pub struct TspWithCoordinates {
    pub name: String,
    pub coordinates: Vec<(f64, f64)>,
}

/// Returns the normalized edge-pair key for two directed edges `(a→b)` and `(c→d)`,
/// placing the lexicographically smaller edge first.
pub(super) fn normalize_edge_pair(e1: TspEdge, e2: TspEdge) -> (TspEdge, TspEdge) {
    if e1 <= e2 { (e1, e2) } else { (e2, e1) }
}

impl TspWithCoordinates {
    pub fn new(name: String, coordinates: Vec<(f64, f64)>) -> Self {
        Self { name, coordinates }
    }

    /// Loads a TSP instance from a TSPLIB-format file.
    pub fn load_file(file_path: &str) -> Result<Self, Box<dyn std::error::Error>> {
        let file = File::open(file_path)?;
        let reader = BufReader::new(file);
        let mut lines = reader.lines();
        let name = lines
            .next()
            .ok_or("File is empty")??
            .split_whitespace()
            .last()
            .ok_or("Not found name")?
            .to_string();

        // skip TYPE
        lines.next();
        // skip COMMENT
        lines.next();
        // skip DIMENSION
        let n = lines
            .next()
            .ok_or("Not found DIMENSION")??
            .split_whitespace()
            .last()
            .ok_or("Not found DIMENSION")?
            .parse::<usize>()?;
        // skip EDGE_WEIGHT_TYPE
        lines.next();
        // skip NODE_COORD_SECTION
        lines.next();

        let mut coord = vec![];
        for _ in 0..n {
            let line = lines.next().ok_or("Not found line")??;
            let mut iter = line.split_whitespace();

            // skip index
            let _ = iter.next();

            let x = iter.next().ok_or("Not found x")?.parse::<f64>()?;
            let y = iter.next().ok_or("Not found y")?.parse::<f64>()?;

            coord.push((x, y));
        }

        Ok(Self::new(name, coord))
    }

    /// Returns the number of cities.
    pub fn get_n(&self) -> usize {
        self.coordinates.len()
    }

    /// Returns the directed edge `(i→j)` at position `k` in the tour.
    pub fn get_edge_from(&self, tour: &TspTour, k: usize) -> TspEdge {
        let n = tour.len();
        (tour[k], tour[(k + 1) % n])
    }

    /// Returns the Euclidean distance between cities `i` and `j`.
    pub fn distance(&self, i: usize, j: usize) -> f64 {
        let (x1, y1) = self.coordinates[i];
        let (x2, y2) = self.coordinates[j];
        ((x1 - x2).powi(2) + (y1 - y2).powi(2)).sqrt()
    }

    /// Adds a new city with the given coordinates to the TSP instance.
    pub fn add_city(&mut self, xy: (f64, f64)) {
        self.coordinates.push(xy);
    }

    /// Returns the 2-opt gain for removing edges `a→b` and `c→d` and adding `a→c` and `b→d`.
    fn calc_2opt_gain_cities(&self, e1: TspEdge, e2: TspEdge) -> f64 {
        let (a, b) = e1;
        let (c, d) = e2;
        self.distance(a, c) + self.distance(b, d) - self.distance(a, b) - self.distance(c, d)
    }

    /// Computes 2-opt gains for all non-adjacent tour edge pairs.
    ///
    /// `gain[normalize(a→b, c→d)]` = change in tour length when removing edges `a→b`
    /// and `c→d` (negative = improvement). Adjacent pairs are excluded.
    pub fn compute_all_gains(&self, tour: &TspTour) -> HashMap<(TspEdge, TspEdge), f64> {
        let n = tour.len();
        let mut gain = HashMap::new();
        for i in 0..n - 1 {
            let e1 = self.get_edge_from(tour, i);
            let max_j = if i == 0 { n - 1 } else { n };
            for j in i + 2..max_j {
                let e2 = self.get_edge_from(tour, j);
                let key = normalize_edge_pair(e1, e2);
                gain.insert(key, self.calc_2opt_gain_cities(e1, e2));
            }
        }
        gain
    }

    /// Removes all gain entries that involve the directed edge `old_a → old_b`.
    ///
    /// Called before modifying the tour to evict stale entries for a removed edge.
    pub fn update_gains_for_removed_edge(&self, sol: &mut TspSolution, old_e: TspEdge) {
        let (old_a, old_b) = old_e;
        let n = sol.tour.len();
        for k in 0..n {
            let e = self.get_edge_from(&sol.tour, k);
            let (c, d) = e;
            if (c, d) == (old_a, old_b) {
                continue;
            }
            if old_b == c || d == old_a {
                continue;
            }
            sol.gain.remove(&normalize_edge_pair(old_e, e));
        }
    }

    /// Inserts gain entries for all non-adjacent pairs involving the new edge `new_a → new_b`.
    ///
    /// Called after modifying the tour to populate entries for a newly added edge.
    pub fn update_gains_for_added_edge(&self, sol: &mut TspSolution, new_e: TspEdge) {
        let (new_a, new_b) = new_e;
        let n = sol.tour.len();
        for k in 0..n {
            let e = self.get_edge_from(&sol.tour, k);
            let (c, d) = e;

            if e == new_e {
                continue;
            }
            if new_b == c || d == new_a {
                continue;
            }
            let key = normalize_edge_pair(new_e, e);
            sol.gain.insert(key, self.calc_2opt_gain_cities(new_e, e));
        }
    }

    /// Calculates the total length of a tour.
    ///
    /// Returns an error if the tour length does not match the number of cities,
    /// or if any city appears more than once.
    pub fn calculate_tour_length(&self, tour: &TspTour) -> Result<f64, OptError> {
        if self.get_n() != tour.len() {
            return Err(OptError::InvalidState(format!(
                "The size of TSP ({}) and length of tour ({}) are not matched",
                self.get_n(),
                tour.len()
            )));
        }

        let mut arrived = HashSet::new();

        let mut length = 0.0;
        for ind in 0..self.get_n() {
            let i = tour[ind];

            if arrived.contains(&i) {
                return Err(OptError::InvalidState(format!(
                    "The city {} is already arrived",
                    i
                )));
            }

            let e = self.get_edge_from(tour, ind);
            length += self.distance(e.0, e.1);

            arrived.insert(i);
        }

        Ok(length)
    }
}

#[cfg(test)]
mod tsp_coord_tests {
    use super::*;

    #[test]
    fn test_load_file() {
        let tsp_result = TspWithCoordinates::load_file("data/tsp/test_data.txt");
        assert!(tsp_result.is_ok());
        let tsp = tsp_result.unwrap();
        assert_eq!(tsp.name, "test");
        assert_eq!(tsp.coordinates.len(), 4);
    }

    #[test]
    fn test_distance() {
        let tsp_result = TspWithCoordinates::load_file("data/tsp/test_data.txt");
        assert!(tsp_result.is_ok());
        let tsp = tsp_result.unwrap();
        assert_eq!(tsp.distance(0, 1), 1.0);
        assert_eq!(tsp.distance(1, 2), 2.0);
    }

    #[test]
    fn test_not_match_size_tour() {
        let tsp_result = TspWithCoordinates::load_file("data/tsp/test_data.txt");
        assert!(tsp_result.is_ok());
        let tsp = tsp_result.unwrap();
        let invalid_tour = vec![0, 1];

        assert!(tsp.calculate_tour_length(&invalid_tour).is_err());
    }

    #[test]
    fn test_duplicated_tour() {
        let tsp_result = TspWithCoordinates::load_file("data/tsp/test_data.txt");
        assert!(tsp_result.is_ok());
        let tsp = tsp_result.unwrap();
        let invalid_tour = vec![0, 1, 0, 2];

        assert!(tsp.calculate_tour_length(&invalid_tour).is_err());
    }

    #[test]
    fn test_tour_length() {
        let tsp_result = TspWithCoordinates::load_file("data/tsp/test_data.txt");
        assert!(tsp_result.is_ok());
        let tsp = tsp_result.unwrap();
        let tour = vec![0, 1, 2, 3];
        assert_eq!(tsp.calculate_tour_length(&tour).unwrap(), 6.0);
    }
}
